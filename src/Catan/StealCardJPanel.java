    public void renderResCards (Graphics g)
    {   
        clickRegions.clear();
        int x     = 10;
        int y     = 20;               
        int count = 0;            
        int inset = this.getInsets().right;
        for (ResourceCard rc:player.resCards)
        {            
            if ((x+rc.getWidth()) >= this.getWidth()-((10*2)+inset))
            {
                x = 10;
                y = y + rc.getHeight() + 10;
            }
            else if (count > 0)
            {
                x = x + (rc.getWidth()/2);  // over lap cards 
            }

            rc.drawAt(null, g, x, y, true, player.gameRules.gameTurnNo); // display as a hidden card
            count++;
            
            // Add/Create a mouse click region to test mouse clicks on particular resource cards
            rc.setLocation(x, y);
        }   
        
        if (player.type == PlayerTypes.COMPUTER)
            vpInfo.setText ("Known Victory Points: " + player.vicPntsPublic);
        else
            vpInfo.setText ("Total Victory Points: " + player.vicPntsTotal);
    }

    @Override
    public void paint(Graphics g)
    {
        super.paint(g);
        renderResCards (g);
    }
    
