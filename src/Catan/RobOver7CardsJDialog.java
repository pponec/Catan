    class ResClickInfo
    {
        ResCardTypes type  = ResCardTypes.NULL;
        int          left  = 0, top    = 0;
        int          right = 0, bottom = 0;
        
        public boolean clicked (Point p)
        {
            if ((p.x>=left &&p.x<=right) &&
                (p.y>=top && p.y <= bottom))
            {
                return true;
            }               
            return false;
        }
    }
