    public void centre (Frame parent)
    {
        if (parent != null)
        {
            Point pp = parent.getLocationOnScreen();
            
            this.setLocation((pp.x + parent.getWidth()/2) - (this.getWidth()/2), 
                             (pp.y + parent.getHeight()/2) - (this.getHeight()/2));
        }
    }   
    
    public void centreToPanel (JComponent p)
    {
        if (p != null)
        {
            Point pp = p.getLocationOnScreen();
            
            this.setLocation((pp.x + p.getWidth()/2) - (this.getWidth()/2), 
                             (pp.y + p.getHeight()/2) - (this.getHeight()/2));
        }
    }      
    
    public void assignPlayer (Player player)
    {
        this.player = player;
        
        if (player.resCards.size() > 7)
            delCount = player.resCards.size() / 2;
        else
            delCount = 0;
        
        repaint();
    }
    
    public void renderResCards (Graphics g)
    {       
        int xOffset = 10;
        int yOffset = actionLabel.getHeight() + 10;
        int count   = 0;
        int maxY    = yOffset;
        ResCardTypes dispOrder[] = {ResCardTypes.SHEEP, ResCardTypes.WHEAT, ResCardTypes.BRICK, ResCardTypes.WOOD, ResCardTypes.ROCK};

        clickRegions.clear();
        for (int idx = 0; idx < dispOrder.length; idx++)
        {
            int yCnt = 0;                                           
            ResourceCard lastRC = null;
            for (ResourceCard rc:player.resCards)
            {
                if (rc.type == dispOrder[idx])
                {
                    if (g != null)
                        rc.drawAt(null, g, xOffset, yOffset + (yCnt * (rc.getHeight()/2)), false, player.gameRules.gameTurnNo);
                    lastRC = rc;
                    yCnt++;
                }
            }                        
            if ((yCnt > 0) && (lastRC != null))
            {
                // Add/Create a mouse click region to test mouse clicks on particular resource cards
                ResClickInfo rci = new ResClickInfo();
                rci.left   = xOffset;
                rci.top    = yOffset;                
                rci.right  = xOffset + lastRC.getWidth();
                rci.bottom = yOffset + ((yCnt+1) * (lastRC.getHeight()/2));
                rci.type   = dispOrder[idx];
                clickRegions.add (rci);               
                
                xOffset = xOffset + (lastRC.getWidth() + 5);
                if (maxY < rci.bottom + 10)
                    maxY = rci.bottom + 10;
            }                        
        }        
        
        this.setSize(this.getWidth(), maxY);

        setActionLabel (player.name + " delete half (" + delCount + ") of your resource cards\nbecause 7 was rolled.");
    }       
    
