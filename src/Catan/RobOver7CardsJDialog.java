    private void formMouseClicked (java.awt.event.MouseEvent evt)//GEN-FIRST:event_formMouseClicked
    {//GEN-HEADEREND:event_formMouseClicked
        if (player == null)
            return;
               
        for (ResClickInfo rci:clickRegions)        
        {           
            if (rci.clicked(evt.getPoint()) != false)
            {
                //remove selected type resource from the player
                for (ResourceCard rc:player.resCards)
                {
                    if (rc.type == rci.type)
                    {   
                        if (delCount > 0)
                        {
                            delCount--;
                            player.resCards.remove(rc);                                                                          
                        }
                        
                        if (delCount <= 0)
                        {
                            completedOK = true;
                            this.setVisible(false); 
                        }
                        
                        this.repaint();
                        return;
                    }
                }

            }                        
        }
        
        if (delCount <= 0)
        {
            completedOK = true;
            this.setVisible(false); 
        }
        
        this.repaint();
    }//GEN-LAST:event_formMouseClicked

    @Override
    public void paint (Graphics g)
    {
        g.clearRect(0, 0, this.getWidth(), this.getHeight());
        
        super.paint(g);
        
        sbb.paintBorder(this, g, 0, 0, this.getWidth(), this.getHeight());
        
        if (player != null)
        {
            renderResCards (g);
        }        
    }
    
    
