    @Override
    public void paint(Graphics g)
    {
        super.paint(g);
        
        Graphics2D g2 = (Graphics2D) g;
        g2.setRenderingHint(RenderingHints.KEY_ANTIALIASING,  RenderingHints.VALUE_ANTIALIAS_ON);
        g2.setRenderingHint(RenderingHints.KEY_INTERPOLATION, RenderingHints.VALUE_INTERPOLATION_BILINEAR);
        g2.setRenderingHint(RenderingHints.KEY_RENDERING,     RenderingHints.VALUE_RENDER_QUALITY);
        
        try
        {       
            // If nothing to draw ... return 
            if (tileList.size() < 2) return;

            // Calculate ySize of the play area to center in screen.
            double diff = ((double) this.getWidth() / (double) hexScaleWidth) * 0.85; // 0.85 == magic number to make it scale correcly to fit every thing in screen, yeah its a hack!

            if (this.getWidth() > this.getHeight())
                diff = ((double) this.getHeight()  / (double)hexScaleWidth) * 0.85;

            Tile tYLen = tileList.get(tileList.size()-2);
            Tile tXLen = tileList.get(tileList.size()-1);

            tYLen.scale(diff, 0, 0);
            tXLen.scale(diff, 0, 0);

            int yLen    = (int) ((tYLen.getBounds2D()).getY() + (tYLen.getBounds2D()).getHeight());
            int xLen    = (int) ((tXLen.getBounds2D()).getX() + (tXLen.getBounds2D()).getWidth());
            int yCentre = (int) ((this.getHeight() - yLen) / 2);
            int xCentre = (int) ((this.getWidth()  - xLen) / 2);

            // ---- Re-scale output ----        
            boolean resized = false;
            if ((lastPaintWidth != this.getWidth()) || (lastPaintHeight != this.getHeight()))
            {
                resized = true;
                for (Tile t : tileList)
                {
                    t.scale(diff, xCentre, yCentre);
                }
                for (BuildPoint bp : buildList)
                {
                    bp.scale(diff, xCentre, yCentre);
                }
                for (Road r : roadList)
                {
                    r.scale(diff, xCentre, yCentre);
                }
                
                lastPaintWidth  = this.getWidth();
                lastPaintHeight = this.getHeight();
            }
            
            // -------------------------------------------------------

            // Create a new static bitmap of the tile background to 
            // speed up updates at the expense of memory.
            if ((resized != false) || (saveTileBG == null))            
            {
                saveTileBG = new BufferedImage (this.getWidth(), this.getHeight(), BufferedImage.TYPE_INT_ARGB);
                Graphics2D stbg_g2 = saveTileBG.createGraphics();
                
                // paint the background 
                if ((this.bgImage != null) && (detailLevelHigh != false))
                {
                    int h = this.getHeight();
                    int w = this.getWidth();
                    for (int y = 0; y < h; y += bgImage.getHeight())
                    {
                        for (int x = 0; x < w; x += bgImage.getWidth())
                        {
                            stbg_g2.drawImage(bgImage, x, y, this);
                        }
                    }
                }

                // ---- Draw Titles -----                       
                for (Tile t : tileList)
                    t.paint(stbg_g2, detailLevelHigh);
                
                // ---- Draw Sea Borders (fill in the cracks) -----       
                // Set the drawing line size 
                Tile tt = tileList.getLast();
                Rectangle2D rect = tt.getBounds2D();
                int width = (int) (rect.getWidth() / 30);
                g2.setStroke(new BasicStroke(width));

                for (Tile t : tileList)
                    t.drawHideBorder(stbg_g2, true, detailLevelHigh);
                
                // ---- Draw Land Borders (fill in the cracks) -----       
                for (Tile t : tileList)
                    t.drawHideBorder(stbg_g2, false, detailLevelHigh);
                
                for (Tile t : tileList)
                    t.drawPorts(stbg_g2, detailLevelHigh);                                                                                             
            }

            // -------------------------------------------------------
            
            // Dump the static background onto the display area.
            g2.drawImage(saveTileBG, 0, 0, null);
                        
            // ---- Draw Roads  -----    
            for (Road r : roadList)
                r.paint(g2);

            // ---- Draw Build Points  -----
            for (BuildPoint bp : buildList)
                bp.paint(g2);

            // Draw high-lighted objects
            if ((hlRender != false) && (hlLastSelObj != null))
            {
                if (hlLastSelObj instanceof BuildPoint)
                {
                    ((BuildPoint)hlLastSelObj).highLightSelectedDraw ((Graphics2D)g, Color.white);
                }
                else if (hlLastSelObj instanceof Road)
                {
                    ((Road)hlLastSelObj).highLightSelectedDraw ((Graphics2D)g, Color.white);
                }
                else if (hlLastSelObj instanceof Tile) // Move Robber ...
                {
                    ((Tile)hlLastSelObj).highLightSelectedDraw ((Graphics2D)g, Color.white);
                }
            }

            // Flash overlay for a freshly changed object (see blinkBGObj).
            if ((blinkOn != false) && (blinkObj != null))
            {
                if (blinkObj instanceof BuildPoint)
                {
                    ((BuildPoint)blinkObj).highLightSelectedDraw ((Graphics2D)g, Color.white);
                }
                else if (blinkObj instanceof Road)
                {
                    ((Road)blinkObj).highLightSelectedDraw ((Graphics2D)g, Color.white);
                }
                else if (blinkObj instanceof Tile)
                {
                    ((Tile)blinkObj).highLightSelectedDraw ((Graphics2D)g, Color.white);
                }
            }
        } catch (Exception e) { e.printStackTrace(); }
    }
