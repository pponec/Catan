private void formMouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseMoved
       
        if (gameWindow == null)
            return;

        if (hlAssistActive == false)
            return;
        
        boolean clearSelObj = false;
        boolean setSelObj   = false;
        
        if (this.gameWindow.gameRules == null)
            return;
        
        if (this.gameWindow.gameRules.thisPlayer == null)
            return;
        
        if (this.gameWindow.gameRules.thisPlayer.type == PlayerTypes.COMPUTER) 
        {
            if (hlRender != false)
            {
                hlLastPlayer = null;
                hlRender     = false;
                hlLastSelObj = null;
                this.repaint();
            }      
            return;            
        }
        
        if (this.gameWindow.gameRules.thisPlayer != hlLastPlayer) 
        {
            hlLastPlayer = null;
            hlRender     = false;
            hlLastSelObj = null;
            this.repaint();            
        }
        
        CatanGraphBase selObj = null;
        LinkedList<Object> l = new LinkedList<Object>();
        Iterator item;

        switch (mouseSelObj)
        {
            case BUILDPOINT:
                l.add((Object) buildList);
                break;

            case ROAD:
                l.add((Object) roadList);
                break;               

            default: // if no in restricted build mode and the high-light areas in 
                     // gameboard you can build/upgrade.
                switch (gameWindow.gameRules.gamePhase)                
                {
                    case PLACE_ROBBER:
                        l.add ((Object)tileList);
                        break;
                        
                    default:
                        // Check if this player can build stuff ..
                        if ((this.gameWindow.gameRules.thisPlayer.canBuild(CanBuildTypes.CITY)       > 0) || 
                            (this.gameWindow.gameRules.thisPlayer.canBuild(CanBuildTypes.SETTLEMENT) > 0))
                            l.add((Object) buildList);

                        if (this.gameWindow.gameRules.thisPlayer.canBuild(CanBuildTypes.ROAD) > 0)
                            l.add((Object) roadList);
                }
        }

        // Process the list ....
        Iterator       il     = l.iterator();
        Polygon p;

        while (il.hasNext() && (selObj == null))
        {
            LinkedList iItem = (LinkedList) il.next();

            item = iItem.iterator();

            while (item.hasNext())
            {
                p = (Polygon) item.next();

                if (p.contains(evt.getPoint()) != false)
                {
                    selObj = (CatanGraphBase)p;
                    break;
                }
            }            
        }             
                        
        // check if object has changed and is a valid selection
        if ((selObj != null) && (selObj != hlLastSelObj))
        {            
            if (selObj instanceof BuildPoint) // high-light buiild areas
            {                             
                switch (this.gameWindow.gameRules.gamePhase)
                {
                    case START_PLACEMENT_1:
                    case START_PLACEMENT_2:
                        // check if we can build a settlement...                
                        if (this.gameWindow.gameRules.isValidBuildPoint ((BuildPoint)selObj, false, this.gameWindow.gameRules.thisPlayer, false) != false)                    
                        {
                            setSelObj = true;
                        } 
                        else
                            clearSelObj = true;
                        break;
                    default:
                        // check if we can build a settlement...                
                        if ((this.gameWindow.gameRules.isValidBuildPoint ((BuildPoint)selObj, true, this.gameWindow.gameRules.thisPlayer, false) != false) &&
                            (this.gameWindow.gameRules.thisPlayer.canBuild(CanBuildTypes.SETTLEMENT) > 0) && 
                            (this.gameWindow.gameRules.thisPlayer.bldStckSettlement > 0))    
                        {
                            setSelObj = true;
                        }                
                        // or upgrade to a city ... 
                        else if ((this.gameWindow.gameRules.thisPlayer.canBuild(CanBuildTypes.CITY) > 0) &&
                                (((BuildPoint)selObj).owner == this.gameWindow.gameRules.thisPlayer)     &&
                                (((BuildPoint)selObj).type  == BuildPointTypes.SETTLEMENT)               &&
                                  (this.gameWindow.gameRules.thisPlayer.bldStckCity > 0))
                        {
                            setSelObj = true;
                        }
                        else
                            clearSelObj = true;                        
                }
            }
            else if (selObj instanceof Road) // high-light road areas 
            {           
                if ((this.gameWindow.gameRules.isValidRoadBuild((Road)selObj, this.gameWindow.gameRules.thisPlayer) != false) &&
                    (this.gameWindow.gameRules.thisPlayer.bldStckRoad > 0))
                    setSelObj = true;
                else
                    clearSelObj = true;
            }           
            else if (selObj instanceof Tile) // Move Robber ... 
            {
                    switch (((Tile)(selObj)).type)
                    {
                        case WOOD:
                        case ROCK:
                        case SHEEP:
                        case WHEAT:
                        case BRICK:
                        case VAR_VOLCANO:
                            if (((Tile)selObj).hasRobber != false)
                                clearSelObj = true;
                            else
                                setSelObj = true;
                            break;
                        default:
                            clearSelObj = true;
                    }
                
            }
            
        }
        else if ((hlRender != false) && (selObj != hlLastSelObj))
            clearSelObj = true;
       
        if (clearSelObj != false)
        {
            hlLastPlayer = null;
            hlRender     = false;
            hlLastSelObj = null;
            this.repaint();                    
        }   
        
        if (setSelObj != false)
        {
            hlLastPlayer = this.gameWindow.gameRules.thisPlayer;
            hlRender     = true;
            hlLastSelObj = selObj;
            this.repaint();            
        }
}//GEN-LAST:event_formMouseMoved

